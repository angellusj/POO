package exercicios.exColecoesGenericas.consultorio;

public class Paciente {
    private int rg;
    private int age;

    public Paciente(int rg, int age) {
        this.rg = rg;
        this.age = age;
    }

    public int getRg() {
        return rg;
    }

    public int getAge() {
        return age;
    }

}
