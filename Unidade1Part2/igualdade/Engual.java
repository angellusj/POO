package Unidade1Part2.igualdade;

public class Engual{
    public static boolean enguals (Integer i1, Integer i2){
        return i1.equals(i2);
    }

    public static boolean enguals (Double d1, Double d2){
        return d1.equals(d2);
    }

    public static boolean enguals (String s1, String s2){
        return s1.equals(s2);
    }
}